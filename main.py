from fastapi import Request

PUBLIC_PATHS = [
    "/",                      # Root
    "/auth/login",            # Login
    "/auth/token",
    "/auth/forgot-password",  # Passwort vergessen
    "/auth/reset-password",   # Passwort zurÃ¼cksetzen
    "/static",                # CSS / Bilder
    "/favicon.ico"
]

@app.middleware("http")
async def auth_redirect_middleware(request: Request, call_next):
    path = request.url.path

    # ğŸ”“ Ã–ffentliche Routen zulassen
    if any(path.startswith(p) for p in PUBLIC_PATHS):
        return await call_next(request)

    # ğŸ” Zugriff nur mit Cookie "access_token"
    token = request.cookies.get("access_token")
    if not token:
        return RedirectResponse(url="/auth/login")

    # âœ… Wenn Token vorhanden â†’ Anfrage weiterleiten
    response = await call_next(request)
    return response